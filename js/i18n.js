  const STORAGE_KEY = "kamir_lang";

  function apply(lang) {
    const dict = DICT[lang] || DICT.he;
    const html = document.documentElement;
    html.setAttribute("lang", lang);
    html.setAttribute("dir", lang === "he" ? "rtl" : "ltr");

    document.querySelectorAll("[data-i18n]").forEach((el) => {
      const key = el.getAttribute("data-i18n");
      if (dict[key] != null) el.innerHTML = dict[key];
    });
    document.querySelectorAll("[data-i18n-placeholder]").forEach((el) => {
      const key = el.getAttribute("data-i18n-placeholder");
      if (dict[key] != null) el.setAttribute("placeholder", dict[key]);
    });
    document.querySelectorAll("[data-i18n-aria]").forEach((el) => {
      const key = el.getAttribute("data-i18n-aria");
      if (dict[key] != null) el.setAttribute("aria-label", dict[key]);
    });

    // Toggle buttons reflect the *other* language
    document.querySelectorAll("[data-lang-toggle]").forEach((btn) => {
      btn.textContent = dict["lang.toggle"];
      btn.setAttribute("aria-label", dict["lang.toggleAria"]);
    });

    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
    window.dispatchEvent(new CustomEvent("langchange", { detail: { lang } }));
  }

  function current() {
    try {
      return localStorage.getItem(STORAGE_KEY) || document.documentElement.getAttribute("lang") || "he";
    } catch (e) {
      return "he";
    }
  }

  // Expose for other scripts (counters, etc.)
  window.KamirI18N = { apply: apply, current: current, dict: DICT };

  // Initial apply (saved preference; default markup is Hebrew)
  apply(current());

  // Wire up toggles
  document.addEventListener("click", function (e) {
    const btn = e.target.closest("[data-lang-toggle]");
    if (!btn) return;
    e.preventDefault();
    apply(current() === "he" ? "en" : "he");
  });
